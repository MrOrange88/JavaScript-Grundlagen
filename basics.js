// Datentypen

let myString = "das ist ein text"; // String

let myNumber = 32; // Number
let myFloat = 32.5; // Float ist eine Zahl mit Komma
let myInteger = 32; // Integer ist eine Zahl ohne Komma

let myBoolean = true; // Boolean (true oder false)

let myArray = ["Apfel", "Banane", "Orange"]; // Array (Liste)

let myObject = {age : 27, name: "Alexander"}; // Object (Objekt) mit key und value