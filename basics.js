
// Variablen mit let    
let myVariable = 27;

myVariable = 32;
// 27 wird überschrieben mit 32
// console.log(meineVariable); // Ausgabe: 32
console.log(myVariable);

// Variablen mit const
const myConst = 27;

// myConst = 32; // Fehler, da const nicht überschrieben werden kann
